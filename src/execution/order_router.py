def _get_bool_setting(name: str, default: bool) -> bool:
    """Read an optional boolean from TRADING config."""
    return bool(getattr(TRADING, name, default))


def _get_float_setting(name: str, default: float) -> float:
    """Read an optional float from TRADING config."""
    try:
        return float(getattr(TRADING, name, default))
    except (TypeError, ValueError):
        return default


def _get_int_setting(name: str, default: int) -> int:
    """Read an optional int from TRADING config."""
    try:
        return int(getattr(TRADING, name, default))
    except (TypeError, ValueError):
        return default


@dataclass
class TradingSignal:
    """A trade signal generated by comparing model output to market prices."""
    side: str           # "BUY_YES" or "BUY_NO"
    token_id: str       # Which token to buy
    price: float        # Limit price to place
    size: float         # Number of shares
    edge: float         # Model edge over market price
    model_prob: float   # Model's predicted probability
    market_price: float # Current market price
    timestamp: float    # When signal was generated


@dataclass
class OrderSizingDecision:
    """Resolved order sizing after venue minimums and bankroll caps."""
    allowed: bool
    size: float
    required_collateral: float
    reason: Optional[str] = None
    available_collateral: Optional[float] = None
    min_order_size: Optional[float] = None


@dataclass
class OrderBookSnapshot:
    """Compact order book summary used for quality filters."""
    best_bid: Optional[float]
    best_ask: Optional[float]
    bid_size_total: float
    ask_size_total: float

    @property
    def imbalance(self) -> Optional[float]:
        total = self.bid_size_total + self.ask_size_total
        if total <= 0:
            return None
        return self.bid_size_total / total

    @property
    def ask_wall_ratio(self) -> Optional[float]:
        if self.bid_size_total <= 0:
            return None
        return self.ask_size_total / self.bid_size_total


