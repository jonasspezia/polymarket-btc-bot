    def evaluate_and_trade(
        self,
        model_prob: float,
        market: MarketInfo,
    ) -> Optional[OrderResult]:
        """
        Core trading decision loop:
        
        1. Fetch best bid/ask from CLOB for both YES and NO tokens
        2. Compare model probability against market prices
        3. If edge > min_edge, place a post-only GTD limit order
        4. Return the order result
        
        Args:
            model_prob: p̂ᵢ — model's probability that BTC goes up
            market: Active MarketInfo with token IDs
            
        Returns:
            OrderResult if an order was placed, None if no edge found.
        """
        signal = self.get_signal(model_prob, market)
        if signal is None:
            return None

        return self.execute_signal(signal, market)

    def get_signal(
        self,
        model_prob: float,
        market: MarketInfo,
    ) -> Optional[TradingSignal]:
        """Return a tradeable signal for the current market, if one exists."""
        yes_book = self._safe_get_order_book(market.yes_token_id)
        no_book = self._safe_get_order_book(market.no_token_id)
        yes_book_snapshot = self._summarize_order_book(yes_book)
        no_book_snapshot = self._summarize_order_book(no_book)

        yes_bid = yes_book_snapshot.best_bid if yes_book_snapshot else None
        yes_ask = yes_book_snapshot.best_ask if yes_book_snapshot else None
        no_bid = no_book_snapshot.best_bid if no_book_snapshot else None
        no_ask = no_book_snapshot.best_ask if no_book_snapshot else None
        if yes_bid is None and yes_ask is None:
            yes_bid, yes_ask = self._client.get_best_bid_ask(market.yes_token_id)
        if no_bid is None and no_ask is None:
            no_bid, no_ask = self._client.get_best_bid_ask(market.no_token_id)

        yes_ask = self._select_dry_run_reference_price(
            live_bid=yes_bid,
            live_price=yes_ask,
            indicative_price=market.indicative_yes_price,
        )
        no_ask = self._select_dry_run_reference_price(
            live_bid=no_bid,
            live_price=no_ask,
            indicative_price=market.indicative_no_price,
        )
        return self._find_signal(
            model_prob,
            market,
            yes_bid,
            yes_ask,
            no_bid,
            no_ask,
            yes_book_snapshot,
            no_book_snapshot,
        )

    def assess_market_executability(self, market: MarketInfo) -> dict[str, Any]:
        """
        Return book-quality diagnostics for discovery-time market filtering.

        A market is considered executable when at least one side has both bid
        and ask quotes with spread at or below the same `max_spread` threshold
        enforced later during signal generation, and whose entry ask does not
        already violate `max_entry_price`.
        """
        yes_snapshot = self._summarize_order_book(
            self._safe_get_order_book(market.yes_token_id)
        )
        no_snapshot = self._summarize_order_book(
            self._safe_get_order_book(market.no_token_id)
        )

        yes_diagnostics = self._build_market_side_diagnostics(yes_snapshot)
        no_diagnostics = self._build_market_side_diagnostics(no_snapshot)
        executable_sides = [
            side
            for side, diagnostics in (
                ("YES", yes_diagnostics),
                ("NO", no_diagnostics),
            )
            if diagnostics["entry_viable"]
        ]
        pathological_sides = [
            side
            for side, diagnostics in (
                ("YES", yes_diagnostics),
                ("NO", no_diagnostics),
            )
            if diagnostics["pathological"]
        ]
        executable = bool(executable_sides)
        best_executable_side = None
        best_executable_spread = None
        best_executable_price = None
        best_executable_mid_price = None
        if executable:
            best_executable_side, best_side_diagnostics = min(
                (
                    ("YES", yes_diagnostics),
                    ("NO", no_diagnostics),
                ),
                key=lambda item: (
                    0 if item[1]["entry_viable"] else 1,
                    item[1]["spread"]
                    if item[1]["spread"] is not None
                    else float("inf"),
                    item[1]["center_distance"],
                    item[1]["best_ask"]
                    if item[1]["best_ask"] is not None
                    else float("inf"),
                ),
            )
            if best_side_diagnostics["entry_viable"]:
                best_executable_spread = best_side_diagnostics["spread"]
                best_executable_price = best_side_diagnostics["best_ask"]
                best_executable_mid_price = best_side_diagnostics["mid_price"]

        return {
            "market": market.slug or market.condition_id,
            "condition_id": market.condition_id,
            "max_spread": self._max_spread,
            "executable": executable,
            "executable_sides": executable_sides,
            "best_executable_side": best_executable_side,
            "best_executable_spread": best_executable_spread,
            "best_executable_price": best_executable_price,
            "best_executable_mid_price": best_executable_mid_price,
            "pathological": (not executable) and bool(pathological_sides),
            "pathological_sides": pathological_sides,
            "yes": yes_diagnostics,
            "no": no_diagnostics,
        }

    def execute_signal(
        self,
        signal: TradingSignal,
        market: MarketInfo,
    ) -> OrderResult:
        """Execute or simulate a previously identified signal."""
        logger.info(
            "Signal found | side=%s edge=%.4f model_p=%.4f market_p=%.4f price=%.2f size=%.4f",
            signal.side,
            signal.edge,
            signal.model_prob,
            signal.market_price,
            signal.price,
            signal.size,
        )

        signal_key = self._build_signal_key(market, signal)
        if self._is_duplicate_signal(signal_key, signal.timestamp):
            self._duplicate_signals_suppressed += 1
            logger.info(
                "Duplicate signal suppressed | market=%s side=%s window=%ds count=%d",
                market.slug or market.condition_id,
                signal.side,
                self._duplicate_window_seconds,
                self._duplicate_signals_suppressed,
            )
            return OrderResult(
                success=False,
                error="duplicate_signal_suppressed",
                raw_response={
                    "reason": "duplicate_signal_suppressed",
                    "market": market.slug or market.condition_id,
                    "side": signal.side,
                    "duplicate_window_seconds": self._duplicate_window_seconds,
                },
            )

        sizing = self._resolve_order_size(signal, market)
        if not sizing.allowed:
            if self._dry_run:
                self._orders_simulated += 1
                logger.warning(
                    "Dry-run signal simulated despite live sizing block | reason=%s "
                    "side=%s price=%.2f size=%.4f #%d",
                    sizing.reason,
                    signal.side,
                    signal.price,
                    signal.size,
                    self._orders_simulated,
                )
                return OrderResult(
                    success=True,
                    order_id=f"dry-run:{market.condition_id}:{signal.side}",
                    raw_response={
                        "dry_run": True,
                        "market": market.slug,
                        "side": signal.side,
                        "price": signal.price,
                        "size": signal.size,
                        "live_blocked": True,
                        "live_block_reason": sizing.reason,
                        "min_order_size": sizing.min_order_size,
                        "available_collateral": sizing.available_collateral,
                    },
                )

            self._orders_rejected += 1
            logger.warning(
                "Order blocked by sizing guard | reason=%s side=%s price=%.2f requested=%.4f "
                "market_min=%s available_collateral=%s #%d",
                sizing.reason,
                signal.side,
                signal.price,
                signal.size,
                (
                    f"{sizing.min_order_size:.4f}"
                    if sizing.min_order_size is not None
                    else "N/A"
                ),
                (
                    f"{sizing.available_collateral:.4f}"
                    if sizing.available_collateral is not None
                    else "N/A"
                ),
                self._orders_rejected,
            )
            return OrderResult(
                success=False,
                error=sizing.reason,
                raw_response={
                    "reason": "order_sizing_blocked",
                    "detail": sizing.reason,
                    "market": market.slug,
                    "side": signal.side,
                    "price": signal.price,
                    "requested_size": signal.size,
                    "resolved_size": sizing.size,
                    "min_order_size": sizing.min_order_size,
                    "available_collateral": sizing.available_collateral,
                },
            )

        if abs(sizing.size - signal.size) > 1e-9:
            logger.info(
                "Adjusted order size | side=%s requested=%.4f resolved=%.4f "
                "required_collateral=%.4f",
                signal.side,
                signal.size,
                sizing.size,
                sizing.required_collateral,
            )
            signal = replace(signal, size=sizing.size)

        if self._dry_run:
            self._remember_signal(signal_key, signal.timestamp)
            simulated_fill = self._dry_run_result_represents_fill(signal)
            self._orders_simulated += 1
            logger.info(
                "Dry-run order simulated | market=%s side=%s price=%.2f size=%.4f "
                "simulated_fill=%s #%d",
                market.slug or market.condition_id,
                signal.side,
                signal.price,
                signal.size,
                simulated_fill,
                self._orders_simulated,
            )
            return OrderResult(
                success=True,
                order_id=f"dry-run:{market.condition_id}:{signal.side}",
                raw_response={
                    "dry_run": True,
                    "market": market.slug,
                    "side": signal.side,
                    "price": signal.price,
                    "size": signal.size,
                    "live_blocked": False,
                    "simulated_fill": simulated_fill,
                },
            )

        if not self._client.has_sufficient_collateral(sizing.required_collateral):
            self._orders_rejected += 1
            logger.warning(
                "Order blocked by collateral guard | required=%.4f side=%s price=%.2f "
                "size=%.2f #%d",
                sizing.required_collateral,
                signal.side,
                signal.price,
                signal.size,
                self._orders_rejected,
            )
            return OrderResult(
                success=False,
                error="Insufficient balance or allowance",
                raw_response={
                    "reason": "insufficient_balance_or_allowance",
                    "required_collateral": sizing.required_collateral,
                    "market": market.slug,
                    "side": signal.side,
                    "price": signal.price,
                    "size": signal.size,
                },
            )

        # Execute the trade
        self._remember_signal(signal_key, signal.timestamp)
        result = self._client.place_post_only_gtd(
            token_id=signal.token_id,
            price=signal.price,
            size=signal.size,
            side="BUY",
            ttl_seconds=self._gtd_ttl,
            neg_risk=market.neg_risk,
        )

        if result.success:
            self._orders_placed += 1
            logger.info(
                "Order executed | id=%s side=%s price=%.2f #%d",
                result.order_id, signal.side, signal.price, self._orders_placed,
            )
        else:
            self._orders_rejected += 1
            logger.warning(
                "Order rejected | error=%s side=%s price=%.2f #%d",
                result.error, signal.side, signal.price, self._orders_rejected,
            )

        return result

    def _resolve_order_size(
        self,
        signal: TradingSignal,
        market: MarketInfo,
    ) -> OrderSizingDecision:
        """Apply venue minimums plus bankroll/notional caps to a signal size."""
        if signal.price <= 0:
            return OrderSizingDecision(
                allowed=False,
                size=0.0,
                required_collateral=0.0,
                reason="Invalid order price",
                min_order_size=market.min_order_size,
            )

        if signal.size <= 0:
            return OrderSizingDecision(
                allowed=False,
                size=0.0,
                required_collateral=0.0,
                reason="Configured order size must be positive",
                min_order_size=market.min_order_size,
            )

        min_order_size = market.min_order_size
        resolved_size = signal.size

        if min_order_size is not None and resolved_size + 1e-9 < min_order_size:
            if not self._allow_upsize_to_min_order_size:
                return OrderSizingDecision(
                    allowed=False,
                    size=resolved_size,
                    required_collateral=signal.price * resolved_size,
                    reason=(
                        "Configured order size is below the market minimum size"
                    ),
                    min_order_size=min_order_size,
                )
            resolved_size = min_order_size

        available_collateral = self._client.get_available_collateral()
        spend_cap = self._resolve_spend_cap(signal.price, available_collateral)
        if spend_cap is not None:
            if spend_cap <= 0:
                return OrderSizingDecision(
                    allowed=False,
                    size=0.0,
                    required_collateral=0.0,
                    reason="No spendable collateral available",
                    available_collateral=available_collateral,
                    min_order_size=min_order_size,
                )

            max_affordable_size = spend_cap / signal.price
            if max_affordable_size + 1e-9 < resolved_size:
                resolved_size = max_affordable_size

            if min_order_size is not None and resolved_size + 1e-9 < min_order_size:
                return OrderSizingDecision(
                    allowed=False,
                    size=max(resolved_size, 0.0),
                    required_collateral=max(resolved_size, 0.0) * signal.price,
                    reason="Available collateral cannot fund the market minimum size",
                    available_collateral=available_collateral,
                    min_order_size=min_order_size,
                )

        resolved_size = max(resolved_size, 0.0)
        required_collateral = resolved_size * signal.price
        if required_collateral <= 0:
            return OrderSizingDecision(
                allowed=False,
                size=0.0,
                required_collateral=0.0,
                reason="Resolved order size must be positive",
                available_collateral=available_collateral,
                min_order_size=min_order_size,
            )

        return OrderSizingDecision(
            allowed=True,
            size=resolved_size,
            required_collateral=required_collateral,
            available_collateral=available_collateral,
            min_order_size=min_order_size,
        )

    def _resolve_spend_cap(
        self,
        price: float,
        available_collateral: Optional[float],
    ) -> Optional[float]:
        """Return the maximum collateral this order may consume, if capped."""
        if price <= 0:
            return 0.0

        spend_cap: Optional[float] = None
        if available_collateral is not None:
            spendable_collateral = max(
                available_collateral - self._reserve_collateral_amount,
                0.0,
            )
            bankroll_fraction = min(max(self._bankroll_fraction_per_order, 0.0), 1.0)
            spend_cap = spendable_collateral * bankroll_fraction

        if self._max_order_notional > 0:
            if spend_cap is None:
                spend_cap = self._max_order_notional
            else:
                spend_cap = min(spend_cap, self._max_order_notional)

        return spend_cap

    def _build_signal_key(self, market: MarketInfo, signal: TradingSignal) -> str:
        """Build a stable deduplication key for a market-side signal."""
        return f"{market.condition_id}:{signal.side}"

    @staticmethod
    def _dry_run_result_represents_fill(signal: TradingSignal) -> bool:
        """
        Treat a dry-run order as fillable only when the simulated entry is
        close enough to the quoted market price to plausibly trade soon.

        Post-only maker orders that sit far below the best ask should be
        tracked as resting diagnostics, not as entered positions.
        """
        tick = float(TRADING.tick_size)
        market_price = float(signal.market_price)
        if market_price <= 0:
            return False
        return signal.price >= max(market_price - tick, tick)

    def _is_duplicate_signal(self, signal_key: str, now: float) -> bool:
        """
        Return True when a similar signal fired too recently.
        Also performs periodic cleanup of the signal cache.
        """
        # Periodic cleanup (every 100 checks)
        if self._orders_placed % 100 == 0:
            self._cleanup_stale_signals(now)

        last_seen = self._recent_signal_times.get(signal_key)
        if last_seen is None:
            return False
        return (now - last_seen) < self._duplicate_window_seconds

    def _cleanup_stale_signals(self, now: float):
        """Remove signals older than the duplicate window to prevent memory leaks."""
        stale_threshold = self._duplicate_window_seconds * 2
        stale_keys = [
            k for k, t in self._recent_signal_times.items() 
            if (now - t) > stale_threshold
        ]
        for k in stale_keys:
            del self._recent_signal_times[k]

    def _remember_signal(self, signal_key: str, now: float):
        """Persist the signal timestamp for duplicate suppression."""
        self._recent_signal_times[signal_key] = now

    def _resolve_signal_size(
        self, price: float, model_prob: Optional[float] = None
    ) -> float:
        """
        Resolve the initial signal size in shares.

        When `ORDER_NOTIONAL` is set, it takes precedence over `ORDER_SIZE` so
        a small bankroll can target a strict dollar spend per trade.

        Improvement 6: When Kelly sizing is enabled, scale the base size by
        fractional Kelly criterion based on model confidence.
        """
        if price <= 0:
            return 0.0

        if self._order_notional > 0:
            base_size = self._order_notional / price
        else:
            base_size = self._order_size

        # Apply fractional Kelly scaling when enabled
        if self._use_kelly_sizing and model_prob is not None and price > 0:
            kelly_size = self._kelly_optimal_fraction(model_prob, price)
            if kelly_size is not None:
                base_size = base_size * kelly_size

        return max(base_size, 0.0)

    def _find_signal(
        self,
        model_prob: float,
        market: MarketInfo,
        yes_bid: Optional[float],
        yes_ask: Optional[float],
        no_bid: Optional[float],
        no_ask: Optional[float],
        yes_book_snapshot: Optional[OrderBookSnapshot] = None,
        no_book_snapshot: Optional[OrderBookSnapshot] = None,
    ) -> Optional[TradingSignal]:
        """
        Check if the model's probability creates a tradeable edge.

        Strategy:
        - If model thinks YES is underpriced (p̂ > best_ask_yes + min_edge):
          → BUY YES at best_bid_yes + 1 tick (or ask, depending on config)
        - If model thinks NO is underpriced ((1-p̂) > best_ask_no + min_edge):
          → BUY NO at best_bid_no + 1 tick
        """
        now = time.time()

        # Improvement 4: Don't enter near market expiry
        if not self._has_sufficient_time_remaining(market):
            logger.info(
                "[SKIP] Not enough time remaining | market=%s min_seconds=%d",
                market.slug or market.condition_id,
                self._min_time_remaining_seconds,
            )
            self._filter_stats["insufficient_time"] += 1
            return None

        # --- Check YES side ---
        if yes_ask is not None and yes_ask > 0:
            # Spread sanity check — wide spreads mean no real liquidity
            yes_spread = (yes_ask - yes_bid) if yes_bid is not None else 1.0
            if yes_spread > self._max_spread:
                logger.info(
                    "[SKIP_YES] Spread too wide | ask=%.4f bid=%s spread=%.4f "
                    "max=%.4f | no real liquidity",
                    yes_ask,
                    f"{yes_bid:.4f}" if yes_bid else "None",
                    yes_spread,
                    self._max_spread,
                )
                self._filter_stats["spread_too_wide"] += 1
            else:
                # Compute the actual entry price — edge is measured against
                # where we'd actually enter, not the raw ask.
                limit_price = self._compute_entry_price(
                    yes_bid, yes_ask, "BUY_YES"
                )
                yes_edge = model_prob - limit_price
                effective_edge = self._min_edge

                if yes_edge < effective_edge:
                    logger.info(
                        "[SKIP_YES] Insufficient edge | model_p=%.4f entry=%.4f "
                        "(ask=%.4f bid=%s) edge=%.4f min_edge=%.4f | need +%.4f more",
                        model_prob,
                        limit_price,
                        yes_ask,
                        f"{yes_bid:.4f}" if yes_bid else "None",
                        yes_edge,
                        effective_edge,
                        effective_edge - yes_edge,
                    )
                    self._filter_stats["edge_too_low"] += 1
                elif model_prob < self._min_side_probability:
                    logger.info(
                        "[SKIP_YES] Model prob too low | model_p=%.4f "
                        "min_side_prob=%.4f | need +%.4f higher",
                        model_prob,
                        self._min_side_probability,
                        self._min_side_probability - model_prob,
                    )
                    self._filter_stats["prob_too_low"] += 1
                elif not self._passes_order_book_filters(
                    market, "BUY_YES", yes_book_snapshot, log_details=True,
                ):
                    pass
                elif not self._passes_entry_price_filter(
                    market, "BUY_YES", limit_price,
                ):
                    pass
                else:
                    size = self._resolve_signal_size(
                        limit_price, model_prob=model_prob
                    )
                    logger.info(
                        "[SIGNAL] YES side accepted | model_p=%.4f entry=%.4f "
                        "(ask=%.4f bid=%s) edge=%.4f min_edge=%.4f size=%.4f",
                        model_prob, limit_price, yes_ask,
                        f"{yes_bid:.4f}" if yes_bid else "None",
                        yes_edge, effective_edge, size,
                    )
                    return TradingSignal(
                        side="BUY_YES",
                        token_id=market.yes_token_id,
                        price=limit_price,
                        size=size,
                        edge=yes_edge,
                        model_prob=model_prob,
                        market_price=yes_ask,
                        timestamp=now,
                    )

        # --- Check NO side ---
        no_model_prob = 1.0 - model_prob
        if no_ask is not None and no_ask > 0:
            no_spread = (no_ask - no_bid) if no_bid is not None else 1.0
            if no_spread > self._max_spread:
                logger.info(
                    "[SKIP_NO] Spread too wide | ask=%.4f bid=%s spread=%.4f "
                    "max=%.4f | no real liquidity",
                    no_ask,
                    f"{no_bid:.4f}" if no_bid else "None",
                    no_spread,
                    self._max_spread,
                )
                self._filter_stats["spread_too_wide"] += 1
            else:
                limit_price = self._compute_entry_price(
                    no_bid, no_ask, "BUY_NO"
                )
                no_edge = no_model_prob - limit_price
                effective_edge = self._min_edge

                if no_edge < effective_edge:
                    logger.info(
                        "[SKIP_NO] Insufficient edge | model_p=%.4f (NO) entry=%.4f "
                        "(ask=%.4f bid=%s) edge=%.4f min_edge=%.4f | need +%.4f more",
                        no_model_prob, limit_price, no_ask,
                        f"{no_bid:.4f}" if no_bid else "None",
                        no_edge, effective_edge, effective_edge - no_edge,
                    )
                    self._filter_stats["edge_too_low"] += 1
                elif no_model_prob < self._min_side_probability:
                    logger.info(
                        "[SKIP_NO] Model prob too low | model_p=%.4f (NO) "
                        "min_side_prob=%.4f | need +%.4f higher",
                        no_model_prob,
                        self._min_side_probability,
                        self._min_side_probability - no_model_prob,
                    )
                    self._filter_stats["prob_too_low"] += 1
                elif not self._passes_order_book_filters(
                    market, "BUY_NO", no_book_snapshot, log_details=True,
                ):
                    pass
                elif not self._passes_entry_price_filter(
                    market, "BUY_NO", limit_price,
                ):
                    pass
                else:
                    size = self._resolve_signal_size(
                        limit_price, model_prob=no_model_prob
                    )
                    logger.info(
                        "[SIGNAL] NO side accepted | model_p=%.4f (NO) entry=%.4f "
                        "(ask=%.4f bid=%s) edge=%.4f min_edge=%.4f size=%.4f",
                        no_model_prob, limit_price, no_ask,
                        f"{no_bid:.4f}" if no_bid else "None",
                        no_edge, effective_edge, size,
                    )
                    return TradingSignal(
                        side="BUY_NO",
                        token_id=market.no_token_id,
                        price=limit_price,
                        size=size,
                        edge=no_edge,
                        model_prob=no_model_prob,
                        market_price=no_ask,
                        timestamp=now,
                    )

        return None

    def _passes_order_book_filters(
        self,
        market: MarketInfo,
        side: str,
        snapshot: Optional[OrderBookSnapshot],
        log_details: bool = False,
    ) -> bool:
        """Reject entries facing obvious order-book resistance.

        Bug 2 fix: for BUY_YES, high bid imbalance = buy pressure = favorable.
        For BUY_NO, high bid imbalance on the NO book means NO-side buy pressure,
        which is also favorable for a BUY_NO entry.  The imbalance filter should
        check whether the side we want to BUY has sufficient buying interest
        (our order adds to bid-side liquidity).  A low imbalance means the ask
        side dominates — unfavorable for a maker buy.
        """
        if snapshot is None:
            return True

        imbalance = snapshot.imbalance
        if imbalance is not None:
            # imbalance = bid_total / (bid_total + ask_total)
            # High imbalance = lots of bids = buy pressure = good for our buy
            # Low imbalance = ask-heavy book = sell pressure = bad for our buy
            if imbalance < self._min_order_book_imbalance:
                logger.info(
                    "[SKIP_%s] Order book imbalance too low | market=%s "
                    "imbalance=%.4f min=%.4f | need +%.4f higher",
                    side.split("_")[1],
                    market.slug or market.condition_id,
                    imbalance,
                    self._min_order_book_imbalance,
                    self._min_order_book_imbalance - imbalance,
                )
                self._filter_stats["imbalance_too_low"] += 1
                return False

        ask_wall_ratio = snapshot.ask_wall_ratio
        if (
            ask_wall_ratio is not None
            and ask_wall_ratio > self._max_ask_wall_ratio
        ):
            logger.info(
                "[SKIP_%s] Ask wall resistance too high | market=%s "
                "ask_wall_ratio=%.4f max=%.4f",
                side.split("_")[1],
                market.slug or market.condition_id,
                ask_wall_ratio,
                self._max_ask_wall_ratio,
            )
            self._filter_stats["ask_wall_too_high"] += 1
            return False

        return True

    def _passes_entry_price_filter(
        self,
        market: MarketInfo,
        side: str,
        price: float,
    ) -> bool:
        """Reject entries that are too expensive relative to capped upside."""
        if self._max_entry_price <= 0 or self._max_entry_price >= 1:
            return True

        if price <= self._max_entry_price + 1e-9:
            return True

        logger.info(
            "[SKIP_%s] Entry price too high | market=%s "
            "price=%.4f max_entry_price=%.4f | need %.4f price reduction",
            side.split("_")[1],
            market.slug or market.condition_id,
            price,
            self._max_entry_price,
            price - self._max_entry_price,
        )
        self._filter_stats["entry_price_too_high"] += 1
        return False

    def _safe_get_order_book(self, token_id: str):
        """Fetch a raw order book snapshot when the client can provide one."""
        # Try the ultra-fast WebSocket cache first
        if self._pm_ws and token_id:
            try:
                book = self._pm_ws.get_book(token_id)
                if book and (book.get("bids") or book.get("asks")):
                    return book
            except Exception as e:
                logger.debug("WS book cache miss for %s: %s", token_id[:12], e)
                
        # Fallback to slow REST API polling
        get_order_book = getattr(self._client, "get_order_book", None)
        if not callable(get_order_book):
            return None
        try:
            return get_order_book(token_id)
        except Exception:
            return None

    def _summarize_order_book(self, book) -> Optional[OrderBookSnapshot]:
        """Summarize the top levels of a book into imbalance metrics."""
        if book is None:
            return None

        bids, asks = self._extract_book_levels(book)
        if bids is None or asks is None:
            return None

        bid_levels = bids[: self._order_book_imbalance_levels]
        ask_levels = asks[: self._order_book_imbalance_levels]
        best_bid = self._best_price(bid_levels, max)
        best_ask = self._best_price(ask_levels, min)

        return OrderBookSnapshot(
            best_bid=best_bid,
            best_ask=best_ask,
            bid_size_total=sum(self._level_size(level) for level in bid_levels),
            ask_size_total=sum(self._level_size(level) for level in ask_levels),
        )

    def _build_market_side_diagnostics(
        self,
        snapshot: Optional[OrderBookSnapshot],
    ) -> dict[str, Any]:
        """Normalize book quality checks into a stable diagnostics payload."""
        best_bid = snapshot.best_bid if snapshot else None
        best_ask = snapshot.best_ask if snapshot else None
        has_quotes = (
            best_bid is not None
            and best_ask is not None
            and best_bid > 0
            and best_ask > 0
        )
        spread = (best_ask - best_bid) if has_quotes else None
        spread_ok = bool(spread is not None and spread <= self._max_spread)
        price_ok = bool(
            has_quotes
            and (
                self._max_entry_price <= 0
                or self._max_entry_price >= 1
                or best_ask <= self._max_entry_price + 1e-9
            )
        )
        at_price_rails = bool(
            has_quotes
            and best_bid <= 0.05
            and best_ask >= 0.95
        )
        extreme_spread = bool(
            spread is not None
            and spread >= max(self._max_spread * 2.0, 0.80)
        )
        pathological = bool((not has_quotes) or at_price_rails or extreme_spread)

        if not has_quotes:
            reason = "missing_quotes"
        elif not spread_ok:
            reason = "spread_too_wide"
        elif not price_ok:
            reason = "price_too_high"
        else:
            reason = None

        if not has_quotes:
            pathology_reason = "missing_quotes"
        elif at_price_rails:
            pathology_reason = "price_rails"
        elif extreme_spread:
            pathology_reason = "extreme_spread"
        else:
            pathology_reason = None

        return {
            "best_bid": best_bid,
            "best_ask": best_ask,
            "spread": spread,
            "spread_ok": spread_ok,
            "price_ok": price_ok,
            "entry_viable": spread_ok and price_ok,
            "reason": reason,
            "pathological": pathological,
            "pathology_reason": pathology_reason,
            "mid_price": (
                ((best_bid + best_ask) / 2.0)
                if has_quotes
                else None
            ),
            "center_distance": (
                abs(((best_bid + best_ask) / 2.0) - 0.5)
                if has_quotes
                else 1.0
            ),
            "bid_size_total": snapshot.bid_size_total if snapshot else 0.0,
            "ask_size_total": snapshot.ask_size_total if snapshot else 0.0,
        }

    @staticmethod
    def _extract_book_levels(book) -> tuple[Optional[list], Optional[list]]:
        if isinstance(book, dict):
            bids = book.get("bids")
            asks = book.get("asks")
            if isinstance(bids, list) and isinstance(asks, list):
                return bids, asks
            return None, None

        bids = getattr(book, "bids", None)
        asks = getattr(book, "asks", None)
        if isinstance(bids, list) and isinstance(asks, list):
            return bids, asks
        return None, None

    @staticmethod
    def _best_price(levels: list, selector) -> Optional[float]:
        prices = [OrderRouter._level_price(level) for level in levels]
        prices = [price for price in prices if price is not None and price > 0]
        if not prices:
            return None
        return selector(prices)

    @staticmethod
    def _level_price(level) -> Optional[float]:
        raw_price = level.get("price") if isinstance(level, dict) else getattr(level, "price", None)
        try:
            return float(raw_price)
        except (TypeError, ValueError):
            return None

    @staticmethod
    def _level_size(level) -> float:
        raw_size = (
            level.get("size", level.get("quantity", 0.0))
            if isinstance(level, dict)
            else getattr(level, "size", getattr(level, "quantity", 0.0))
        )
        try:
            return max(float(raw_size), 0.0)
        except (TypeError, ValueError):
            return 0.0

    @staticmethod
    def _snap_price(price: float) -> float:
        """
        Snap a price to the Polymarket tick size (0.01).
        Rounds down to nearest tick for post-only compliance.

        Bug 1 fix: previously subtracted an extra tick after snapping,
        costing ~2% edge per trade on a 50¢ market.
        """
        tick = float(TRADING.tick_size)
        # Round down to nearest tick — this alone ensures post-only
        snapped = int(price / tick) * tick
        return max(round(snapped, 2), 0.01)

    def _compute_entry_price(
        self,
        best_bid: Optional[float],
        best_ask: Optional[float],
        side: str,
    ) -> float:
        """
        Improvement 7: Compute the entry limit price.

        When bid-side entry is enabled, place at best_bid + 1 tick instead
        of snapping down from the ask. This provides price improvement on
        every fill while still sitting on the maker side of the book.
        """
        tick = float(TRADING.tick_size)

        if (
            self._use_bid_side_entry
            and best_bid is not None
            and best_bid > 0
            and best_ask is not None
            and best_ask > 0
        ):
            spread = best_ask - best_bid
            if spread > tick + 1e-9:
                # Place at best_bid + 1 tick (inside the spread)
                entry = best_bid + tick
                return max(round(entry, 2), 0.01)

        # Fallback: snap to the ask
        if best_ask is not None and best_ask > 0:
            return self._snap_price(best_ask)
        if best_bid is not None and best_bid > 0:
            return self._snap_price(best_bid)
        return 0.01

    def _compute_effective_min_edge(self, model_prob: float) -> float:
        """
        Improvement 1 + 5: Dynamic minimum edge.

        Scales higher when the model is near certainty (less room for error)
        and stays at base level for balanced predictions.
        """
        # Confidence scaling: require more edge near the boundaries
        confidence_penalty = 2.0 * abs(model_prob - 0.5)
        return self._min_edge * (1.0 + confidence_penalty)

    def _has_sufficient_time_remaining(self, market: MarketInfo) -> bool:
        """
        Improvement 4: Don't enter positions near market expiry.

        Late entries have less time for the underlying to move, but the same
        binary payout risk. Near expiry, market prices converge to true
        probability, leaving no exploitable edge.
        """
        if self._min_time_remaining_seconds <= 0:
            return True

        end_ts = GammaAPIClient._parse_iso_timestamp(market.end_date)
        if end_ts is None:
            return True

        time_remaining = end_ts - time.time()
        if time_remaining < self._min_time_remaining_seconds:
            logger.debug(
                "Skipping entry — too close to expiry | market=%s "
                "remaining=%.0fs min=%ds",
                market.slug or market.condition_id,
                time_remaining,
                self._min_time_remaining_seconds,
            )
            return False
        return True

    def _kelly_optimal_fraction(
        self, model_prob: float, entry_price: float
    ) -> Optional[float]:
        """
        Improvement 6: Fractional Kelly criterion sizing.

        f* = (p * b - q) / b
        where p = win probability, q = 1-p, b = net payout odds.
        Capped at `kelly_fraction` (default 25%) for safety.
        """
        if entry_price <= 0 or entry_price >= 1:
            return None

        # Binary market payout: pay `entry_price`, receive 1.0 on win
        b = (1.0 - entry_price) / entry_price  # net payout odds
        p = model_prob
        q = 1.0 - p

        if b <= 0:
            return None

        kelly_f = (p * b - q) / b
        if kelly_f <= 0:
            return 0.0  # Negative Kelly means don't bet

        # Apply fractional Kelly cap
        return min(kelly_f, self._kelly_fraction)

    def _select_dry_run_reference_price(
        self,
        live_bid: Optional[float],
        live_price: Optional[float],
        indicative_price: Optional[float],
    ) -> Optional[float]:
        """
        In dry-run mode only, fall back to indicative market pricing when the
        live book is missing or clearly unusable for paper validation.
        """
        if not self._dry_run:
            return live_price

        if indicative_price is None or indicative_price <= 0:
            return live_price

        if live_price is None or live_price <= 0:
            return indicative_price

        if live_bid is None or live_bid <= 0:
            return live_price

        if (live_price - live_bid) >= 0.20:
            return indicative_price

        return live_price
